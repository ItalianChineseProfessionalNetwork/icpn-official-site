                        <div class="entry-content text-justify">
                            <p id="1">
                            Secondo un’analisi condotta <a href="http://uis.unesco.org/sites/default/files/documents/fs55-women-in-science-2019-en.pdf">da UNESCO nel 2019</a>, solo il 29,3% del personale in ricerca e sviluppo per materie STEM (Science, Technology, Engineering and Mathematics) è di genere femminile. L’Italia si pone ventisettesima in Europa con il 35,2% di donne in materie STEM, mentre la Cina si pone diciassettesima nella classifica dei paesi asiatici con una rappresentanza femminile pari al 39,1%. 
                        </p>
                        <br/>
                        <p>Uno studio del <a href="https://openknowledge.worldbank.org/bitstream/handle/10986/34317/Main-Report.pdf?sequence=1&isAllowed=y">The World Bank nel 2020</a> dimostra come <b>pregiudizi e stereotipi</b> contribuiscano a disincentivare le donne a perseguire una carriera <b>nel settore STEM</b>.
Lo studio dimostra come la percentuale di ragazzi e di ragazze che completano la scuola primaria sia la stessa e che, nelle materie scientifiche, le ragazze ottengono punteggi uguali o superiori rispetto ai ragazzi. (<b>Tab 1</b>) Così come ci sono più laureate donne rispetto agli uomini. Tuttavia, la probabilità che una donna persegua una carriera STEM è significativamente più bassa. (<b>Tab 2</b>)
</p>

                            <h3 id="2">Se le ragazze hanno voti migliori e le donne hanno un tasso di istruzione più alto, a cosa è dovuto questo divario di genere? </h3>

                            <p>
                                Esaminiamo alcune delle cause:
                                <ul>
                                    <li>Un filone di prove indica come gli stereotipi e i pregiudizi partano dalle aule di scuola e dai <b>materiali educativi. </b></li>
                                    <li>Pregiudizi e <b>stereotipi nascono anche in casa</b>. I sondaggi rivelano che i genitori mostrano una maggiore preferenza e supporto verso i figli maschi a lavorare nelle STEM. (<b>Tab 3</b>)</li>
                                    <li>A livello globale, le donne subiscono più <b>discriminazioni sul posto di lavoro</b> soprattutto nei settori a prevalenza maschile. (Casi di discriminazioni riportate: 50% donne, meno del 20% uomini). (<b>Tab 4</b>)</li>
                                </ul><br/>
                                Tali pregiudizi minano l’autostima, scoraggiano e ostacolano le donne a perseguire una carriera scientifica. 
                            </p>
                            <p>
                            Lo stesso studio raccomanda di affrontare i pregiudizi di genere <b>a partire dalla scuola, coinvolgendo i genitori</b> e incoraggiando <b>attività extrascolastiche</b> che hanno il potenziale di promuovere atteggiamenti positivi e di ispirare maggiore fiducia sia tra i ragazzi che tra le ragazze. Importante è anche la partecipazione del <b>settore privato</b> tramite sostegno finanziario e opportunità di lavoro. Ultimo ma non meno importante, è la presenza di <b>role models</b> in grado di rappresentare un modello di ispirazione. 
                            </p>
                            <p>
                            Uno di questi modelli è la dottoressa Tu Youyou, vincitrice nel 2015 del premio Nobel per la Medicina per i suoi preziosi studi sulla cura della malaria. Si tratta della prima donna della Cina continentale ad aver vinto il Nobel in una categoria scientifica.
                            </p>
                            <p>
                            La ricercatrice dell’Accademia di Medicina Tradizionale Cinese è stata premiata per le scoperte sull’artemisinina, un principio attivo naturale particolarmente efficace per debellare la malattia infettiva. I suoi studi affondano le radici nella medicina tradizionale cinese e si sono sviluppati partendo dall’analisi di testi dell’antica Cina. <br/>
                            Nel 1969, all’età di 39 anni, Tu venne nominata a capo del “Project 523” un istituito per far fronte alle gravi epidemie che affliggevano vaste aree del continente asiatico ed in particolare il Vietnam del Nord. Per analizzare da vicino la malattia, la ricercatrice si recò sull’isola di Hainan, nel sud della Cina, le cui foreste pluviali erano infestate dalla pericolosa zanzara anofele.
Il gruppo di scienziati riuscì ad isolare un composto della pianta di assenzio, ma fu Tu ad intuire che per evitare la distruzione del principio attivo, la molecola doveva essere estratta a bassa temperatura in un liquido differente dall’acqua. 
<br/>La scoperta fu pubblicata in inglese solo nel 1979 iniziando ad attirare l’attenzione internazionale. Bisognerà comunque aspettare l’inizio del XXI secolo affinché l’OMS raccomandi la terapia messa appunto dalla ricercatrice come primo strumento contro la lotta alla malaria. La scoperta dell’artemisinina è stata definita come una tra le ricerche farmaceutiche più importanti dell’ultimo mezzo secolo.<br/>
La dottoressa Tu Youyou rappresenta un modello di tenacia e di grande coraggio. La posizione al vertice del gruppo di ricerca e l’importante sfida che ha dovuto fronteggiare testimoniano intelligenza, perseveranza e coraggio. La ricercatrice ha dedicato la sua vita al progresso e alla ricerca scientifica. Fu la prima volontaria a testare il farmaco di sua invenzione. Grazie al lavoro compiuto dalla dottoressa Tu, oltre duecento milioni di pazienti affetti da malaria hanno potuto ricevere terapie salvavita.
                            </p>
