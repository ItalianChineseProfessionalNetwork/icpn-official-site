                        <div class="entry-content text-justify">
                            <p>
                            Per prima cosa ti ringrazio Fabio a nome mio e della nostra associazione per la disponibilità. È un onore, oltre
che piacere, avere la possibilità di ascoltare un manager del tuo calibro su un tema tanto importante quanto
sull’onda dell’innovazione tecnologica: l’educazione e il suo futuro.
                        </p>
                       
                        <p id="1"><strong>Dopo esserci conosciuti nel 2018 in Google Italia, mentre tu eri CEO e Vice-President del Board EMEA
- di che cosa ti sei occupato e quali sono state le considerazioni che ti hanno portato nel mondo
dell'educazione?</strong>
</p>

                            <p>
                                Sono convinto che la formazione sia un elemento fondamentale per lo sviluppo delle persone e della capacità
di un Paese di crescere e rinnovarsi. Per questo ho scelto di iniziare un nuovo percorso passando da Google a
Multiversity e quindi dal mondo dell’high tech e della trasformazione digitale a quello della ricerca scientifica,
della produzione di contenuti accademici e dello sviluppo di strumenti per la crescita individuale e collettiva.
D’altronde in uno scenario come quello attuale, in cui contenuti e tecnologia sono sempre più interconnessi,
vi è una certa continuità tra la mia esperienza passata e quella presente. Con 5 miliardi di persone in rete nel
mondo, e oltre 35 miliardi di device connessi, siamo nel pieno di una rivoluzione digitale che ha cambiato ogni
aspetto delle nostre vite, ogni settore e attività, una trasformazione che riguarda in particolare il mondo
dell’informazione, quello del lavoro, delle competenze e, di conseguenza, la formazione.
                            </p>
                            
                            <p id="2"><strong>
                            La pandemia ha fatto accelerare l'adozione delle tecnologie digitali, soprattutto in Italia. Ad oggi vedi
ancora delle barriere o difficoltà da superare?</strong>
                            </p>
                            <p>
                           Certamente la pandemia ha provocato una forte spinta nella direzione di una maggiore diffusione della
tecnologia in ogni ambito. Eppure bisogna essere consapevoli che molte soluzioni adottate in quel difficile
momento erano emergenziali, quindi non sempre adeguate e senza dubbio perfettibili. Pensiamo ad esempio
alla DAD, un’esperienza che va considerata nell’ambito di un perimetro molto preciso, ossia quello di una
necessità urgente e improvvisata, vissuta con difficoltà non solo dagli studenti ma dagli stessi docenti che non
erano preparati a insegnare in modalità online e soprattutto erano privi degli strumenti adeguati.
L’apprendimento digitale richiede piattaforme concepite e strutturate appositamente. Fare didattica online non
significa solo mettersi davanti a una telecamera e registrare. Va ripensata l’intera proposta formativa attraverso
il grande potenziale dei nuovi strumenti, con modalità interattive che rendono lo studente parte integrante del
processo di apprendimento. Oggi i giovani vivono in una dimensione che vede totale continuità e integrazione
tra lo spazio fisico e quello digitale, comunicano con gli smartphone e attraverso i social network, prendono
appunti con il tablet e utilizzano la realtà aumentata. Dobbiamo essere consapevoli che il mondo sta
cambiando, che è in corso una trasformazione antropologica, cognitiva e sociale. Eppure in Italia sul fronte
della cultura digitale stiamo ancora arrancando e scontiamo enormi ritardi. Secondo l’ultimo Report Desi siamo
20esimi in Europa per livello di digitalizzazione e 25esimi per la diffusione delle competenze digitali.
                            </p>
                            
                            <p id="3"><strong>Quali sono i punti di forza e quali da migliorare o integrare di un'educazione digitale?</strong>
                            
                            </p>
                            
                            <p>
                                Sono convinto che l’educazione digitale, grazie alla sua flessibilità e accessibilità, rappresenti uno strumento
fondamentale per la crescita personale e collettiva. La tecnologia applicata alla formazione aiuta infatti a
potenziare l’offerta, ad adeguare il linguaggio e a modulare i contenuti in forme che consentono di raggiungere
un numero di persone enorme, arrivando in aree non servite da istituzioni accademiche tradizionali e
consentendo di strutturare in modo personalizzato i contenuti e l’offerta, senza rinunciare alla qualità.
L’istruzione diventa così profondamente inclusiva e democratica e apre la strada a una reale mobilità sociale,
a vantaggio dell’intero sistema produttivo. Da noi purtroppo la consapevolezza sul valore della didattica
digitale, molto diffusa nella formazione anglosassone, risente ancora di qualche pregiudizio. Eppure il trend è
chiaro. Gli studenti chiedono sempre di più una didattica moderna e gli Istituti di formazione hanno una
responsabilità enorme: devono essere capaci di rielaborare le modalità di trasmissione del sapere per evitare
gap formativi e abbandono scolastico. Solo un’integrazione strategica fra atenei in presenza e digitali, pubblici
e privati, può colmare il divario nel numero di laureati in Italia rispetto al resto d’Europa. Un gap che è fonte
di disuguaglianze sociali e causa di livelli di produttività inferiori a quelli di altri Paesi.

                            </p>
                            
                     <p id="4"><strong>In una tua recente intervista a Vanity Fair ho avuto modo di leggere: «Era un'anomalia italiana la
mancanza della doppia laurea, la possibilità di frequentare un doppio percorso universitario, finalmente
è stata colmata», posso chiederti che cosa significa questo per la singola persona che sta per
intraprendere un percorso di studi universitario e per la società?</strong></p>

<p>L'introduzione della doppia laurea in Italia ha disegnato un sistema flessibile che supera rigidità e vincoli
normativi a favore di una formazione trasversale che risponde alle necessità del presente. L’iscrizione
contemporanea a due corsi di laurea, non necessariamente nello stesso ateneo, permette agli studenti di
sviluppare percorsi di studio realmente aderenti alle inclinazioni e al talento personale e allo stesso tempo
capaci di rispondere a esigenze del mercato del lavoro sempre più multidisciplinari. Viviamo in un mondo in
cui il paradigma della società industriale che vedeva separate rigidamente le forme di conoscenza - scientifica
e umanistica - si è irrimediabilmente incrinato. Nella realtà di oggi, complessa e in costante evoluzione, la
formazione deve essere continua, l’aggiornamento incessante e la conoscenza integrata. Non si può ragionare
in compartimenti stagni ma è necessario essere in grado di creare link e connessioni tra discipline e saperi
diversi. Come Multiversity siamo orgogliosi di aver accolto il primo studente in Italia ad aver usufruito di
questa possibilità, con una doppia iscrizione al corso di laurea in ingegneria informatica presso l’Università
Mercatorum e al corso in Giurisprudenza presso l’Università Telematica Pegaso.</p>

<p i="5"><strong>Condivido l'enfasi rivoluzionaria della doppia laurea. La sua implementazione è un grande primo
passo per superare il paradigma che un individuo possa e debba concentrarsi solo su un'unica materia,
molto spesso o umanistica o scientifica. Quali materie credi, che se congiuntamente studiate, possano
essere rilevanti nei prossimi anni?</strong></p>
<p>Certamente ritengo sia importante seguire le proprie inclinazioni, guardando allo stesso tempo con attenzione
le esigenze del mercato del lavoro che sono in continuo cambiamento. Ma le competenze trasversali delle quali
ormai non si può fare più a meno sono quelle digitali. Si tratta di apprendere le regole di un nuovo alfabeto:
non basta sapere usare uno smartphone ma bisogna essere consapevoli del mezzo, delle sue potenzialità e dei
suoi rischi. L’alfabetizzazione digitale non può prescindere da un processo di responsabilizzazione verso
questa nuova dimensione. Parliamo di competenze ormai indispensabili e sempre più articolate. Basti pensare
che da una recente indagine, realizzata da Unioncamere e Anpal con il contributo del Centro Studi delle Camere
di commercio G. Tagliacarne, emerge che per il 95,1% degli ingressi nell’ambito delle figure professionali
dirigenziali si ritiene indispensabile il possesso della conoscenza digitale e che in particolare, ai gruppi
professionali legati a profili dirigenziali, intellettuali, tecnici ed esecutivi, oltre alle competenze digitali di base,
è richiesta proprio la capacità di utilizzare linguaggi e metodi matematici e informatici.</p>


<p id="6"><strong>Quali sono i punti di forza dell’educazione italiana? E quali spunti si potrebbero prendere dagli altri
sistemi educativi?</strong></p>

<p>In Italia certamente vantiamo un impianto teorico di primo livello. Ci fregiamo di Università antichissime e di
Istituzioni culturali di grande prestigio. Per non parlare dei talenti, giovani studenti e professionisti preparati e
in gamba che purtroppo ancora spesso si trovano a doversi spostare all’estero per vedere riconosciute le proprie
abilità. Abbiamo bisogno di attivare un canale forte con il mondo del lavoro, permettere agli studenti di
sperimentarsi presto in contesti professionali e fare tanta esperienza. Da questo punto di vista le Università
digitali forniscono una risposta. Consentono ad esempio di continuare a lavorare studiando, in una vera e
propria ottica lifelong learning. Nei nostri Atenei il collegamento con il mondo del lavoro è molto forte, il
consolidato rapporto di collaborazione con le Camere di Commercio e la nuova partnership siglata con il
Gruppo Sole 24 Ore garantiscono una vicinanza unica al sistema delle imprese, anche grazie a sinergie per la
realizzazione di academies con contenuti formativi co-progettati e prodotti.
</p>

<p id="7"><strong>In un <a href="https://conditiohumana.io/prometheus-and-the-last-fire/" target="_blank">articolo&#185</a> pubblicato da Conditio Humana nel 2019, sottolineavo che con l'avvento delle nuove
tecnologie (Intelligenza Artificiale, Machine Learning), il vantaggio competitivo e punto di forza degli
esseri umani si celano nella nostra intelligenza associativa e creatività. Quali sono per te i prossimi passi
da prendere nell'educazione futura?</strong></p>
<p>La formazione vede al centro l’essere umano, l’individuo e la sua creatività. Applicare l’innovazione
tecnologica alla didattica è un modo per confermare questo concetto. Con le nuove risorse, la formazione
diventa ancora più aderente alle inclinazioni e alle ambizioni personali. È importante assicurare che ognuno
abbia a disposizione strumenti adeguati a sviluppare le proprie capacità e alimentare il proprio talento. Su
questo aspetto, il modello didattico di erogazione delle Università Digitali è assolutamente conforme al
perseguimento degli obiettivi di inclusione e sviluppo sostenibile previsti dell’Agenda 2030 delle Nazioni
Unite, in particolare per quanto riguarda la riduzione delle disuguaglianze e la garanzia a un ampio e sicuro
accesso a un'istruzione di qualità. La missione di Multiversity è la modernizzazione dell’istruzione superiore
in Italia, abbiamo l’ambizione di alzare di molto l’asticella della qualità nell’interesse di chi studia, migliora e
contribuisce alla crescita collettiva. Per questo siamo determinati ad attrarre e coinvolgere nel nostro progetto
i migliori talenti e le migliori risorse del Paese.</p>
