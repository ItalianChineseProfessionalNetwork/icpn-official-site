                                    <p>
                                        <b><a href="https://na.leagueoflegends.com/en-gb/" target="_blank">League of
                                                Legends</b></a>, conosciuto anche con l’anagramma <b>LoL</b>, è un
                                        videogioco sviluppato dalla <b>Riot Games</b> nel <b>2008.</b>
                                    </p>

                                    <p>
                                        Il gioco consiste in due squadre composte da 5 componenti. Prima di iniziare una
                                        partita, ogni giocatore sceglierà il proprio personaggio da un pool
                                        <b>Champions,</b> ognuno con abilità diverse, il proprio personaggio, che dovrà
                                        pilotare con <b>mouse</b> e <b>tastiera</b> per tutta la durata della partita.
                                    </p>

                                    <p>
                                        Come in ogni altro sport, i giocatori di una squadra dovranno <b>collaborare</b>
                                        insieme, attuare strategie e sfruttare le proprie sinergie per raggiungere
                                        l’obiettivo del gioco: distruggere la base nemica, difendendo la propria.
                                    </p>

                                    <p>
                                        Mentre nel <b>calcio</b> abbiamo attaccanti, centrocampisti, difensori e
                                        portieri,
                                        anche in <b>LoL</b> abbiamo dei ruoli:
                                    </p>
