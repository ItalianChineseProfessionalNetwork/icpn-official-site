                                    <h3>3. Come si diventa giocatori professionisti: chiediamolo a Shadow!</h3>

                                    <p>
                                        Come abbiamo detto nel paragrafo precedente, al mondo ci sono oltre <b>2,7
                                            miliardi di videogiocatori</b>. Eppure, solo un limitatissimo numero riesce
                                        ad andare a competere a livello <b>professionale.</b>
                                    </p>

                                    <p>
                                        Ma quindi <u><b>come si diventa un “pro”?</b></u> Noi di <b>ICPN (Italian
                                            Chinese Professional Network)</b> abbiamo avuto il piacere di intervistare
                                        <b><a href="https://lol.gamepedia.com/Shad0w" target="_blank">Shadow (Zhiqiang
                                                Zhao)</a></b>, jungler dei <b>MAD LIONS</b>, squadra posizionata al
                                        <b>primo posto</b> della classifica europea di <b>League of Legends!</b>
                                    </p>

                                    <h5><b>Raccontaci la tua storia!</b></h5>

                                    <p>
                                        Ciao! Mi chiamo Zhiqiang Zhao, ma sono conosciuto come <b>Shadow</b>. Ho 19 anni
                                        e attualmente abito a Berlino, dove <b>lavoro</b> e mi <b>alleno</b> con il Team
                                        <b>MAD LIONS</b>.
                                    </p>

                                    <p>
                                        La <b>passione per i videogiochi</b> è nata in prima superiore. Era come
                                        sfuggire
                                        dalla realtà per me, una valvola di sfogo. Per questo passavo la maggior parte
                                        del mio tempo al pc.
                                    </p>

                                    <p>
                                        I miei genitori inizialmente erano molto contrari alla mia scelta, in quanto
                                        preferivano la <b>strada normale:</b> studiare, andare all’università e trovarmi
                                        un lavoro.<br>
                                        Come loro, sapevo che questa non era la via più facile. Temevano che
                                        <b>prendessi una brutta strada,</b> <u>ma io sapevo che il percorso “normale”
                                            non faceva per me.</u>
                                    </p>

                                    <p>
                                        Ricordo che ho voluto <b>impegnarmi</b> a realizzare il mio sogno per
                                        <b>dimostrare ai miei genitori</b> che potevo guadagnare soldi facendo ciò che
                                        amavo fare, ed infatti eccomi qui.
                                    </p>


                                    <p>
                                        Ho iniziato la <b>carriera da professionista</b> intorno ai 16 anni giocando
                                        nella scena competitiva italiana per un anno. In seguito mi sono inserito nel
                                        campionato cinese di League of Legends (il cosiddetto <b>LPL, League of Legends
                                            Pro League</b>). <br>
                                        Tuttavia non mi sono trovato molto bene, in quanto l’approccio che si ha verso i
                                        giocatori è molto diverso. <br>
                                        Si concentrano molto sulla pratica in game, giocando moltissime ore al giorno ed
                                        inoltre visto che ci sono molti più ragazzi che ambiscono a diventare
                                        professionisti, le squadre non si fanno molti problemi a sostituirti.<br>
                                        Così non si crea un bell’ambiente all’interno del team.
                                    </p>

                                    <p>
                                        Quest’anno sto facendo parte dei <b>MAD Lions</b>, un team che gioca nel
                                        campionato Europeo di League of Legends ( chiamato <b>LEC, League of Legends
                                            European Championship</b>) e mi sto trovando molto bene sia con lo staff che
                                        con i compagni di squadra, anche perché si è creato un forte legame all’interno
                                        del gruppo e quindi c’è molta sinergia tra di noi.
                                    </p>

                                    <p>
                                        La mia <b>giornata lavorativa</b> inizia alle 12:00 dove ci si incontra con
                                        tutto
                                        il team in ufficio e si fa un meeting. Successivamente passiamo agli allenamenti
                                        dalle 14:00 fino alle 19:00.
                                    </p>

                                    <p>
                                        Abbiamo la sera libera, e sovente la passo parlando del più e del meno con gli
                                        amici o a fare altro per alleviare lo stress accumulato durante la giornata.
                                    </p>

                                    <p>
                                        Oltre agli allenamenti al pc che facciamo quotidianamente, abbiamo anche altre
                                        attività da svolgere per migliorare le nostre prestazioni. <br>
                                        Abbiamo ogni settimana almeno due sessioni di allenamento fisico.<br>
                                        La nostra dieta è esattamente uguale a quella di uno sportivo professionista:
                                        riso bianco, pollo al vapore e verdure, con la possibilità di fare un Cheat Day
                                        a settimana!
                                    </p>

                                    <p>
                                        Lo staff della squadra è molto ampio, oltre ai membri del team che giocano le
                                        partite ci sono 3 coach che analizzano tutte le possibili combinazioni e ci
                                        aiutano a stare al passo con i continui aggiornamenti del gioco. <br>
                                        Al di fuori del gioco in sè ci sono 1 manager, 2 psicologi/mental coach e 1
                                        fisioterapista, per poter mantenere il livello psicofisico sempre al top.
                                    </p>

                                    <h5><b>Consigli per chi vuole intraprendere la stessa ambizione che hai avuto?</b></h5>

                                    <p>Per farsi notare dai recruiters dei migliori teams, è intanto necessario
                                        dimostrare le proprie capacità nella classifica <i>SoloQ</i>. Per chi non lo
                                        sapesse, ogni giocatore ha la possibilità di giocare delle partite
                                        <b>classificate</b> che andranno a determinare la sua posizione all’interno
                                        della classifica generale dei migliori giocatori del server a cui appartieni (ad
                                        esempio North America, Europe West, Korea ecc…). <br>
                                        Più sei in alto nella classifica, più possibilità hai di essere contattato e di
                                        fare il provino per entrare nella squadra.
                                    </p>

                                    <p>
                                        Nel settore del gaming competitivo sono stato a contatto con delle persone
                                        brillanti e super preparate che mi hanno permesso di maturare molto. inoltre ho
                                        iniziato a vivere lontano dalla famiglia, diventando più autonomo e
                                        responsabile.
                                    </p>

                                    <p>
                                        Se dovessi dare dei consigli a chi vorrebbe intraprendere la carriera del
                                        giocatore professionista direi innanzitutto di credere molto in se stessi!
                                        Quello dell’E-Sport è un ambiente estremamente competitivo e sicuramente è una
                                        strada dove la maggior parte delle volte non si è supportati dalla famiglia e/o
                                        dagli amici, quindi devi <b>credere in te stesso, sempre! </b>
                                    </p>

                                    <p>
                                        Il secondo consiglio che posso dare è di fissarti un periodo di tempo preciso,
                                        magari 1 o 2 anni, dove concentrare tutte le tue forze e provare a diventare
                                        giocatore professionista.
                                        Finito il tempo prestabilito vedi dove sei arrivato e quanto ti manca per
                                        raggiungere il tuo obiettivo. <br>
                                        Se noti di avere ancora troppa strada davanti, non c’è assolutamente niente di
                                        male nel cambiare strada! Il professionismo ad alti livelli, purtroppo, non è
                                        per tutti, tuttavia è ancora più importante essere consapevoli sulla proprie
                                        capacità e sulla propria strada!<br>
                                        Indipendentemente da quali risultati raggiungerete, impegnatevi sempre a fare
                                        scelte e sacrifici che rimpiangerete in futuro!
