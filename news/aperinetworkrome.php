                                La serata ha avuto un grande successo ed è stata arricchita dalla presenza di tre guest speakers d’onore: Elisa Wong, Kevin Lin e Kevin Zhu.

                            </p>
                            
                            <p id="2">Elisa Wong, dopo il suo percorso di studi presso l’Università LUISS, ha intrapreso una carriera nel mondo del cinema. Ha esordito in diversi spot pubblicitari prima di approdare alle serie TV e al cinema. Oggi è una giovane attrice molto promettente, parte del cast della serie “Love Club” disponibile su Amazon Prime Video e della terza stagione della fiction di successo “Doc-Nelle tue mani” su Rai 1.
                            </p>
                            <p>
                           Durante l’evento, Elisa ha condiviso la sua esperienza e ha parlato del suo ritrovato amore per la recitazione durante gli studi. Ha sottolineato l’importanza di avere mentori, specialmente in un settore come il cinema dove ancora mancano figure di riferimento sino-italiane. In merito ha infatti evidenziato il ruolo fondamentale dei mentori per guidare e supportare i giovani durante la loro crescita professionale.

                            </p>
                            
                            
                            
                            </p>
                            
