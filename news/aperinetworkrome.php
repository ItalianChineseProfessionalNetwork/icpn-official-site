                            </p>
                            
                     <p id="4">Oggi, con la crescita di questa nuova platea di professionisti di origine cinese, che condividono il background multiculturale come quello cinese e italiano, Kevin crede fortemente che si apriranno numerose opportunità, soprattutto per attrarre ingenti investimenti dalla Cina verso l’Italia.
</p>

<p>Kevin Zhu, d’altro canto, è un imprenditore che gestisce uno studio contabile e una software house, fornendo servizi a diversi settori in tutta Italia, tra cui ristoranti, magazzini, bazar e ingrossi. Per lui, il network, il confronto e, in generale, il mentorship sono elementi di grande importanza, di cui ha riscontrato l’importanza in quanto non ha avuto la possibilità di accedervi durante il suo percorso professionale passato. Ha condiviso quanto ha lavorato duramente per arrivare dove si trova oggi e ha riconosciuto le immense potenzialità della nuova generazione. Kevin ha concluso il suo discorso evidenziando come la nuova generazione, grazie a tutte queste risorse e iniziative, potrà godere di prospettive estremamente positive.
</p>


